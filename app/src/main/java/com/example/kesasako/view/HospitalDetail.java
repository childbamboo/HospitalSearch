public class HospitalDetail {
        private String name;
        private String tel;

        public HospitalDetail(String name, String tel) {
            this.name = name;
            this.tel = tel;
        }

        public String getName() {
            return name;
        }

        public String getTel() {
            return tel;
        }
}
